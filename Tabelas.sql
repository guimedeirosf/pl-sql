/*** DROP ***/
DROP TABLE FUNCIONARIO;
DROP TABLE DEPARTAMENTO;
DROP TABLE CARGO;

/*** CREATE ***/
-- CARGO
CREATE TABLE CARGO (
       CodCargo           NUMBER(5) NOT NULL,
       NomCargo           VARCHAR2(50),
       PRIMARY KEY (CodCargo)
);

-- DEPARTAMENTO
CREATE TABLE DEPARTAMENTO (
       CodDep             NUMBER(5) NOT NULL,
       NomDep             VARCHAR2(50),
       Gerente            NUMBER(5),
       PRIMARY KEY (CodDep)
);

-- FUNCIONARIO       
CREATE TABLE FUNCIONARIO (
    CodFun               NUMBER(5) NOT NULL,
    NomFun               VARCHAR2(70) ,
    CodDep               NUMBER(5) REFERENCES DEPARTAMENTO(CodDep),
    Ramal                NUMBER(4),
    DatAdm               DATE,
    CodCargo             NUMBER(3) REFERENCES CARGO(CodCargo),
    Sexo                 VARCHAR2(1) CHECK (Sexo IN ('F', 'M')),
    DatNasc              DATE,
    Salario              NUMBER(9,2),
    PRIMARY KEY (CodFun)
);

